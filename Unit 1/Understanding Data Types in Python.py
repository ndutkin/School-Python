# Literal (Basic values in Python, like numbers or single characters.)
x = 10
a = 20.2
d = a
print("Literal:", x, a, d)

# Integer (Whole numbers that can be positive, negative, or zero.)
y = -20
b = 0
print("Integer:", y, b)

# Float (Numbers that have a decimal point.)
z = 15.5
c = 123.456
print("Float:", z, c)

# String (Sequence of characters enclosed in single or double quotes.)
first = "Nathan"
last = "Dutkin"
age = "112"
print("My name is:", first, last)
print("My age is:", age)

# Boolean (Values that represent either true or false.)
is_active = True
is_fullscreen = False
is_windowed = 0
print("Boolean:", is_active, is_fullscreen, is_windowed)

# List of a Literal, Interger, FLoat, String, and Boolean.
print("Some Random Values:", x, y, z, first, is_active)